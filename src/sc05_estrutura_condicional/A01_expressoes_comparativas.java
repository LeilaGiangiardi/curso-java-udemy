package src.sc05_estrutura_condicional;

public class A01_expressoes_comparativas {

	public static void main(String[] args) {
		
		// Suponha x igual a 5
		int x = 5;
		
		System.out.println("--- Exemplos de ExpressÃµes Comparativas ---");
		
		// x > 0 -> Resultado: V (true)
		System.out.println("x > 0: " + (x > 0)); 
		
		// x == 3 -> Resultado: F (false)
		System.out.println("x == 3: " + (x == 3)); 
		
		// 10 <= 30 -> Resultado: V (true)
		System.out.println("10 <= 30: " + (10 <= 30)); 
		
		// x != 2 -> Resultado: V (true)
		System.out.println("x != 2: " + (x != 2)); 
		
    }
}